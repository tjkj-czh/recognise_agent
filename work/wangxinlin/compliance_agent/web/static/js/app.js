function loadParcels() {
  fetch("/api/parcels")
    .then((r) => r.json())
    .then((geojson) => {
      parcelsDataSource = new Cesium.GeoJsonDataSource("parcels");
      parcelsDataSource.load(geojson, {
        stroke: Cesium.Color.fromCssColorString("#333333"),
        strokeWidth: 2,
        fill: Cesium.Color.GRAY.withAlpha(0.4),
        clampToGround: false,
      }).then(() => {
        // 按风险等级着色每个entity
        const entities = parcelsDataSource.entities.values;
        for (const entity of entities) {
          const risk = entity.properties.overall_risk_level?.getValue() || "无特殊关注";
          const color = RISK_COLORS[risk] || RISK_COLORS["无特殊关注"];
          if (entity.polygon) {
            entity.polygon.material = color;
          }
        }
        viewer.dataSources.add(parcelsDataSource);
      });
    })
    .catch((err) => console.error("加载图斑失败:", err));

  // 点击事件
  const handler = new Cesium.ScreenSpaceEventHandler(viewer.scene.canvas);
  handler.setInputAction((click) => {
    const picked = viewer.scene.pick(click.position);
    if (Cesium.defined(picked) && picked.id) {
      const entity = picked.id;
      onParcelClick(entity);
    }
  }, Cesium.ScreenSpaceEventType.LEFT_CLICK);
}

function onParcelClick(entity) {
  const parcelId = entity.properties.parcel_id?.getValue();
  if (!parcelId) return;

  // 高亮
  if (highlightedEntity && highlightedEntity.polygon) {
    const prevRisk = highlightedEntity.properties.overall_risk_level?.getValue() || "无特殊关注";
    highlightedEntity.polygon.material = RISK_COLORS[prevRisk] || RISK_COLORS["无特殊关注"];
  }
  if (entity.polygon) {
    entity.polygon.material = Cesium.Color.fromCssColorString("#FFFF00").withAlpha(0.7);
  }
  highlightedEntity = entity;

  // 飞到该图斑
  viewer.flyTo(entity, { duration: 1.5 });

  // 获取报告
  showLoading();
  fetch(`/api/parcel/${parcelId}`)
    .then((r) => r.json())
    .then((report) => renderResult(report))
    .catch((err) => showError(err));
}

// === 结果渲染 ===
function renderResult(report) {
  persistLatestReport(report);
  const container = document.getElementById("resultContent");
  if (!container) return;

  let html = `<div class="result-header">
    <div class="parcel-id">${report.parcel_id}</div>
    <div class="parcel-meta">${report.summary ? report.summary.split("。")[0] : ""}</div>
    <span class="risk-badge risk-badge-${report.overall_risk_level}">${report.overall_risk_level}</span>
  </div>`;

  if (report.summary) {
    html += `<div class="result-section">
      <div class="result-section-title">摘要</div>
      <p class="summary-text">${report.summary}</p>
    </div>`;
  }

  if (report.auto_judgments && report.auto_judgments.length > 0) {
    html += `<div class="result-section">
      <div class="result-section-title attention">自动判断 (${report.auto_judgments.length})</div>`;
    report.auto_judgments.forEach((text) => {
      html += `<div class="rule-card" data-type="关注">${text}</div>`;
    });
    html += `</div>`;
  }

  if (report.suspected_prompts && report.suspected_prompts.length > 0) {
    html += `<div class="result-section">
      <div class="result-section-title suspected">疑似提示 (${report.suspected_prompts.length})</div>`;
    report.suspected_prompts.forEach((text) => {
      html += `<div class="rule-card" data-type="疑似">${text}</div>`;
    });
    html += `</div>`;
  }

  if (report.manual_review_items && report.manual_review_items.length > 0) {
    html += `<div class="result-section">
      <div class="result-section-title review">人工复核项 (${report.manual_review_items.length})</div>`;
    report.manual_review_items.forEach((item) => {
      const priClass = item.priority === "高" ? "high" : "medium";
      html += `<div class="rule-card review-card" data-type="建议复核">
        <span class="review-priority ${priClass}">${item.priority}优先</span>${item.item}
        <div class="review-reason">${item.reason}</div>
      </div>`;
    });
    html += `</div>`;
  }

  if (report.generated_at) {
    html += `<div class="generated-at">生成时间: ${report.generated_at}</div>`;
  }

  container.innerHTML = html;
}

