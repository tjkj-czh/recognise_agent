async function loadTerrain() {
  try {
    const tp = await Cesium.ArcGISTiledElevationTerrainProvider.fromUrl(
      "https://elevation3d.arcgis.com/arcgis/rest/services/WorldElevation3D/Terrain3D/ImageServer"
    );
    viewer.terrainProvider = tp;
    viewer.scene.globe.depthTestAgainstTerrain = true;
    console.log("[地形] ArcGIS 全球高程加载成功");
  } catch (e) {
    console.warn("[地形] 加载失败，使用默认平地:", e);
  }
}

function addStarField() {
  const starCount = 5000;
  const starDistance = 1e8;
  const points = new Cesium.PointPrimitiveCollection();

  for (let i = 0; i < starCount; i++) {
    const theta = Math.random() * 2 * Math.PI;
    const phi = Math.acos(2 * Math.random() - 1);
    const x = starDistance * Math.sin(phi) * Math.cos(theta);
    const y = starDistance * Math.sin(phi) * Math.sin(theta);
    const z = starDistance * Math.cos(phi);

    points.add({
      position: new Cesium.Cartesian3(x, y, z),
      color: new Cesium.Color(
        0.8 + Math.random() * 0.2,
        0.8 + Math.random() * 0.2,
        1.0,
        0.6 + Math.random() * 0.4
      ),
      pixelSize: Math.random() * 1.5 + 0.5,
    });
  }
  viewer.scene.primitives.add(points);
}

